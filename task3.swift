//Задание 5
//
//Необходимо реализовать функцию, которая принимает номер дня недели и возвращает его тип.
//
//Если номер дня от 1 до 5 включительно — это рабочий день. Если номер равен 6 или 7 — это выходной. Во всех остальных случаях необходимо вернуть сообщение об ошибке.
//
//После реализации функции необходимо вызвать ее в цикле для значений от 1 до 8 и вывести результаты в консоль.
func getWeekDayType(dayNumber: Int) -> String {
    return (1...5).contains(dayNumber) ? "рабочий день" : (6...7).contains(dayNumber) ? "выходной" : "неверный номер дня"
}

for i in 1...8 {
    print("День №\(i) - это \(getWeekDayType(dayNumber: i))")
}

//Задание 6
//
//Реализуйте функцию, которая определяет, является ли число простым.
//
//Используя данную функцию, выведите в консоль все простые числа в диапазоне от 1 до 20.
func isPrime(_ number: Int) -> Bool {
    guard number > 1 else { return false }
    for i in 2..<number {
        if number.isMultiple(of: i) { return false }
    }
    return true
}

for number in 1...20 {
    if isPrime(number) {
        print("\(number) - простое число")
    }
}
