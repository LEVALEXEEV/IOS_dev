//Результаты анализа необходимо вывести в консоль.
let array = [1, 2, 3, 4, 5, -5, -2, -1, 0, 0, 0, 100, 300, 500]

var positiveCount: Int = 0
var negativeCount: Int = 0
var zeroCount: Int = 0
var maxValue: Int = Int.min
var totalSum: Int = 0

for i in array {
    if i > 0 {
        positiveCount += 1
        maxValue = max(maxValue, i)
    }
    else if i < 0 { negativeCount += 1 }
    else { zeroCount += 1 }
    totalSum += i
}

print("""
Количество:
    • положительных чисел: \(positiveCount)
    • отрицательных чисел: \(negativeCount)
    • нулевых значений: \(zeroCount)
Максимальное значение в массиве: \(maxValue)
Cумма всех элементов: \(totalSum)
""")

